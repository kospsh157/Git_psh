// go는 워크스페이스가 정해져있다. 따라서 위의 폴더를 반드시 있어야한다.
// 윈도우의 경우 c:\Go 폴더이며 맥의 경우 /user/go 폴더이다.

// 그리고 고는 모듈을 정해진 한 곳에서만 받는게 아니다 node처럼 npm처럼 정해진 한군데에서만 받는게 아니기 때문에 
// 스스로 고 폴더 밑에다가 어디서 받았는지 정리할 수 있게 도메인폴더들을 작성해야 한다.
// 그리고 이 강의에서는 /user/go/src/ 폴더 밑에 도메인별로 정리하라고 추천하고 있다.
// 따라서 /user/go/src/github.com    /golang.org    /google.golang.org  이렇게 src폴더 밑에 도메인별로 폴더를 작성하자.

